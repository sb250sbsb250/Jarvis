TEMPLATE_PATTERN = re.compile(r'\{\{\s*(\w+)\s*\}\}')


def render_template(variables: Dict[str, str], base_system: str = "", mode: str = "coding") -> str:
    """渲染 system prompt 模板。根据 mode 选择对应模板，coding 使用默认模板。"""
    if mode and mode != "coding":
        from .modes import get_mode_config
        mode_cfg = get_mode_config(mode)
        if mode_cfg.system_template is not None:
            template = mode_cfg.system_template
        else:
            template = _BASE_TEMPLATE
    else:
        template = _BASE_TEMPLATE

    if base_system:
        template = template + "\n\n" + base_system

    def replacer(match):
        var_name = match.group(1)
        value = variables.get(var_name)
        if value is None:
            return ' {{ ' + var_name + ' }} '
        return str(value)

    return TEMPLATE_PATTERN.sub(replacer, template)
